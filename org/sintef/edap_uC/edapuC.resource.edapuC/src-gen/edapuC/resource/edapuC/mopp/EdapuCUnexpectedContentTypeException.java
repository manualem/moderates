package edapuC.resource.edapuC.mopp;

/**
 * An Excpetion to represent invalid content types for parser instances.
 * 
 * @see edapuC.resource.edapuC.IEdapuCOptions.RESOURCE_CONTENT_TYPE
 */
public class EdapuCUnexpectedContentTypeException extends org.antlr.runtime3_2_0.RecognitionException{
	
	private static final long serialVersionUID = 4791359811519433999L;
	
	private Object contentType = null;
	
	public  EdapuCUnexpectedContentTypeException(Object contentType) {
		this.contentType = contentType;
	}
	
	public Object getContentType() {
		return contentType;
	}
	
}
